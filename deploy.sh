    error "MySQL –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω. –£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ MySQL"
    echo "–í—ã–ø–æ–ª–Ω–∏—Ç–µ: sudo apt install mysql-server -y"
    exit 1
fi

