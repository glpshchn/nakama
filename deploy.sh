    error "Node.js –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω. –£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ Node.js 18.x"
    echo "–í—ã–ø–æ–ª–Ω–∏—Ç–µ: curl -fsSL https://deb.nodesource.com/setup_18.x | sudo -E bash -"
